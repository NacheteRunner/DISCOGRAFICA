    public  ArrayList<String> transformarTabla (){
        
        ArrayList<String> datos = new ArrayList<>();
        
        for (int i=0; i< modelo.getRowCount();i++){
            String linea = "";
            for( int j=0; j<modelo.getColumnCount();j++){
                linea+=modelo.getValueAt(i, j)+";";
            }
            datos.add(linea);
        }
        
        return datos;
    }
    
    
    
    public void cargarTabla(){
        ArrayList<String> datos = new ArrayList<>();
        datos = AccesoADatos.leerDatosFichero();
        for (int i=0; i<datos.size();i++){
            String linea = datos.get(i);
            String[] registros = linea.split(";");
            modelo.addRow(registros);
            
        }
    }
    
    public boolean insertarRegistroTabla(){  // metodo a usar con el boton nuevo
        // metodo que recoja los 4 registros de los campos del formulario e inserte en tabla
        // Llamar a validar campos primero if(!validarCampos) return false;
        if (validar_campos()){
            String tema = tf_tema.getText();
            String autor = tf_autor.getText();
            String album = tf_album.getText();
            String anno = String.valueOf(jYearChooser1.getValue());
        
            String[] registros = {tema, autor, album, anno};
        
            modelo.addRow(registros);
        
            limpiar_campos();
            return true;
        }
        return false;
    }
    
    public void borrarRegistroTabla(int row){ // Metodo boton borrar
        if (row!=-1){
            modelo.removeRow(row);
        }
    }
    
    public boolean modificarRegistroTabla(int row){
        //Llamar a validar campos primero if(!validarCampos) return false
        if (validar_campos()){
            if (row!=-1){
                String tema = tf_tema.getText();
                String autor = tf_autor.getText();
                String album = tf_album.getText();
                String anno = String.valueOf(jYearChooser1.getValue());
                String[] registros = {tema, autor, album, anno};
                modelo.removeRow(row);
                modelo.insertRow(row, registros);
                limpiar_campos();
            }else{
                return false;
        }
        return true;
        }
        return false;
    }
    
    private void iniciarTabla(){
        
        modelo = new DefaultTableModel();
        modelo.addColumn("Titulo");
        modelo.addColumn("Autor");
        modelo.addColumn("Album");
        modelo.addColumn("AÃ±o publicaciÃ³n");
        
        tbl_canciones.setModel(modelo);
        
        cargarTabla();
        
        
        
    }
    
    
    

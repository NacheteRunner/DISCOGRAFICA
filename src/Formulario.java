    private void limpiar_campos() {
        tf_album.setText("");
        
        tf_autor.setText("");
        tf_tema.setText("");
    }
    
    private boolean validar_campos(){
        if ((tf_tema.getText()==null)||(tf_tema.getText().contentEquals(""))){
            JOptionPane.showMessageDialog(null, "Debes escribir un nombre en el campo Tema");
            return false;
        }
        if ((tf_album.getText()==null)||(tf_album.getText().contentEquals(""))){
            JOptionPane.showMessageDialog(null, "Debes escribir un nombre en el campo Album");
            return false;
        }
        if ((tf_autor.getText()==null)||(tf_autor.getText().contentEquals(""))){
            JOptionPane.showMessageDialog(null, "Debes escribir un nombre en el campo Autor");
            return false;
        }
        
        return true;
    }
    
